    private void fillTableToTable() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSachXe.getModel();
        model.setRowCount(0);
        try {
            String maxe = txtKeys.getText();
            String tenxe = txtKeys.getText();
            List<Xe> list  = daoXe.selectByKeyword(maxe, tenxe);
            
            for (Xe xe : list) {
                daoHangXe.selectAll();
                Object[] row = {
                    xe.getMaXe(),
                    xe.getTenXe(),
                    xe.getMaHang(),
                    xe.getMaLoai(),
                    xe.getTonKho(),
                    xe.getDinhMucNhoNhat(),
                    xe.getDinhMucLonNhat(),
                    xe.getGiaBan(),
                    xe.getGiaNhap(),
                    xe.getBaoHanh(),
                    xe.isTrangThai()?"Còn hàng":"Hết hàng"
                };
                model.addRow(row);
            }
        } catch (Exception e) {
        }
    }
