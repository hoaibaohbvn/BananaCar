    private void clearForm() {
        try {
            Xe model = new Xe();
            this.setForm(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    private void insert() {
        Xe xe = getFormThemXe();
        
        try {
            
            daoXe.insert(xe);
            this.fillTableToTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm thành công \n Tồn kho = 0 \n Trạng thái: Hết hàng");
        } catch (Exception e) {
            e.printStackTrace();
            MsgBox.alert(this, "Thêm thất bại");
        }

    }
    
    void edit() {
        try {
            String maXe = (String) tblDanhSachXe.getValueAt(this.rows, 0);
            
            Xe model = daoXe.selectById(maXe);
            if (model != null) {
                this.setForm(model);
                
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
