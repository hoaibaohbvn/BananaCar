    private void fillTableToTable() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSachXeGanHet.getModel();
        model.setRowCount(0);
        try {
            
            List<Xe> list  = daoXe.selectAllXeGanHet();
            
            for (Xe xe : list) {
                Object[] row = {
                    xe.getMaXe(),
                    xe.getTenXe(),
                    xe.getTonKho(),
                    xe.getDinhMucNhoNhat()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
        }
    }
